# Day 42 - String Indexing

word = "Python"

print("Word:", word)

# Positive Indexing
print("First Character:", word[0])
print("Third Character:", word[2])
print("Last Character (Positive):", word[len(word) - 1])

# Negative Indexing
print("Last Character:", word[-1])
print("Second Last Character:", word[-2])

# Length
print("Length:", len(word))

# String Comparison
print("A < a :", "A" < "a")
print("Z < a :", "Z" < "a")
print("b > a :", "b" > "a")
print("2 > 1 :", "2" > "1")
print("apple < apply :", "apple" < "apply")

# Information Check
text = "coding"
print("Last Character:", text[-1])

